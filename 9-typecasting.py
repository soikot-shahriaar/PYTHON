# # Typecasting in python
# Conversion of one data type into the other data type 

# Python supports a wide variety of functions or methods like: int(), float(), str(), ord(), hex(), oct(), tuple(), set(), list(), dict(), etc. for the type casting in python.

# # Explicit typecasting:
# conversion of one data type into another data type, done by programmer.

a = "1"
b = "2"
print(int(a) + int(b))

# # Implicit TypeCasting:
# one data type is converted into other by the Python interpreter itself (automatically).
c = 1.9
d = 8
print(c + d)
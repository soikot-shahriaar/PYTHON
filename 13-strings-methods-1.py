# Strings are Immutable
a = " .!!!Soikot!!! !!!!!!!!! Shahriar"
print(len(a)) # length of a string
print(a)
print(a.upper())  # converts a string to upper case.
print(a.lower())  # converts a string to lower case.
print(a.strip())  # removes any white spaces before and after the string.
print(a.replace("Soikot", "Skt")) #replaces a string with another string.
print(a.split(" ")) #splits the given string at the specified instance and returns the separated strings as list items.
blogHeading = "introduction tO my Blog."
print(blogHeading.capitalize()) #turns only the first character of the string to uppercase